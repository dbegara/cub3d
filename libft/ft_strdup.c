#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	int		len;

	len = ft_strlen(s1) + 1;
	if (!(str = malloc(len)))
		return (0);
	ft_strlcpy(str, s1, len);
	return (str);
}
